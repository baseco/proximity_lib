-module(proximity_lib_app).

-behaviour(application).

%% Application callbacks
-export([start/2, stop/1]).

%%====================================================================
%% API
%%====================================================================

start(_StartType, _StartArgs) ->
    case proximity_lib_sup:start_link() of
        {ok, _Pid} = Ret ->
            proximity_lib_pg:start(),
            Ret;
        Error ->
            Error
    end.

%%--------------------------------------------------------------------
stop(_State) ->
    ok.

